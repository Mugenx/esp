  <span style="font-size: 5rem; margin-bottom: 50px;">Our History</span>
  <div class="col-lg-10" style="float: none; margin: 0 auto" >
    
    <img src="img/IMG_8136ac.png" style="width: 250px; float: left; margin-right:40px">

      <p style="color: white; text-align: justify;">
        Verney Conference Management (“VCM”) was founded in 2002. Over the past 
        15 years, the company has grown to produce over 250 events from coast to 
        coast.<br><br>

        Dustin Rivers, the company's co-Founder, was working in Toronto for what 
        he calls a "conference production factory, that prioritized quantity of 
        events over quality of agenda, with little regard for the communities 
        the events were supposed to serve". He was quickly able to identify an 
        opportunity, in large part due to the feedback he was receiving about 
        the conferences he was currently producing. With an eye towards working 
        with the public sector to produce the same type of high quality events, 
        but at drastically less cost per delegate, Dustin packed up his meager 
        belongings and moved to Ottawa to start Verney Conference Management.<br><br>

        It wasn't until 2003 that things really began to take off, and Dustin 
        began formulating a new Event Production Business Model - what he now 
        calls Community-Based Developed events. In April of that year, VCM was 
        joined by now Partner and Co-owner, Chris Lutz.<br><br>

        Over this time, as Dustin and Chris honed their negotiation skills and 
        learned more about the financial intricacies of the event industry. VCM 
        took a "let's try to do it ourselves before paying someone else" 
        mentality and quickly became a one-stop shop for complete Event 
        Production.  Not only did the team at VCM manage event logistics along 
        with its associated vendors, it procured an AV arsenal capable of 
        servicing a 300+ person event with 5 concurrent sessions running at any 
        given time. VCM also began supplying in-house Graphic Design services, 
        but perhaps most impressively of all, they created what is now known as 
        EventSystemPro - VCM's custom built event registration and event 
        management system.<br><br>

        Built by event planners for event planners.
      </p>

